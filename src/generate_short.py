def parse_script(text: str) -> List[Dict]:
    """
    Parse script into segments with speaker assignments.

    Supports formats:
        NARRATOR:                      → narration block
        Some narration text here.

        CHARACTER_NAME:                → dialogue block
        "Dialogue in quotes."

        CHARACTER (note):              → dialogue with parenthetical
        "More dialogue."

        Or inline: "Text" said Character.

    Returns list of dicts: {type, text, speaker, voice}
    """
    segments = []
    lines = text.strip().split('\n')

    current_speaker = "narrator"
    buffer = []

    for line in lines:
        line = line.strip()

        # Skip empty lines, decorative lines, and END markers
        if not line or re.match(r'^[═─━▬\-=*]{3,}$', line):
            if buffer:
                _flush_buffer(buffer, current_speaker, segments)
                buffer = []
            continue

        if re.match(r'^(End of Part|END OF PART|#)', line, re.I):
            continue

        if re.match(r'^PART\s+\d', line, re.I):
            continue

        if re.match(r'^FORMATTING NOTES|^The system will|^Format guidelines', line, re.I):
            break  # Stop at formatting notes section

        # Check if this line is a speaker label: "CHARACTER_NAME:" or "CHARACTER (note):"
        speaker_match = re.match(
            r'^([A-Z][A-Za-z\'\s]+?)(?:\s*\([^)]*\))?\s*:\s*$', line
        )
        if speaker_match:
            if buffer:
                _flush_buffer(buffer, current_speaker, segments)
                buffer = []
            current_speaker = normalize_speaker(speaker_match.group(1))
            continue

        # Check if line is "SPEAKER: inline dialogue"
        inline_match = re.match(
            r'^([A-Z][A-Za-z\'\s]+?)(?:\s*\([^)]*\))?\s*:\s*(.+)', line
        )
        if inline_match:
            if buffer:
                _flush_buffer(buffer, current_speaker, segments)
                buffer = []
            current_speaker = normalize_speaker(inline_match.group(1))
            buffer.append(inline_match.group(2))
            continue

        # Regular content line
        buffer.append(line)

    # Flush remaining buffer
    if buffer:
        _flush_buffer(buffer, current_speaker, segments)

    # If parsing produced nothing, try fallback paragraph-based parsing
    if not segments:
        segments = _parse_fallback(text)

    return segments


def _flush_buffer(buffer: list, speaker: str, segments: list):
    """Process accumulated buffer lines into segment(s)."""
    text = ' '.join(buffer).strip()
    if len(text) < 5:
        return

    text = clean_text(text)

    # Even if labelled as NARRATOR, we check for embedded dialogue
    if speaker.lower() == "narrator":
        if '"' in text:
            segments.extend(_parse_mixed_text(text))
        else:
            segments.append({
                "type": "narration",
                "text": text,
                "speaker": "narrator",
                "voice": config.VOICES.get("narrator", config.VOICES["_default"]),
            })
    else:
        if text.startswith('"') and text.endswith('"'):
            text = text[1:-1].strip()

        voice = config.VOICES.get(speaker, config.VOICES["_default"])
        segments.append({
            "type": "dialogue",
            "text": text,
            "speaker": speaker,
            "voice": voice,
        })


def _parse_fallback(text: str) -> List[Dict]:
    """Fallback parser for unformatted text."""
    paragraphs = [p.strip() for p in re.split(r'\n{2,}', text) if p.strip()]
    segments = []

    for para in paragraphs:
        if re.match(r'^(#|Part \d|End of Part|═|─)', para, re.I):
            continue
        
        segments.extend(_parse_mixed_text(para))

    return segments


def _parse_mixed_text(text: str) -> List[Dict]:
    """Splits a block of text into narration and dialogue segments."""
    segments = []
    verb_pattern = '|'.join(config.SAID_VERBS)
    remaining = text
    
    while remaining:
        # Match dialogue in quotes
        quote = re.search(r'"([^"]{3,})"', remaining)
        if not quote:
            if len(remaining.strip()) > 5:
                segments.append({
                    "type": "narration", "text": clean_text(remaining),
                    "speaker": "narrator",
                    "voice": config.VOICES["narrator"]
                })
            break

        before = remaining[:quote.start()].strip()
        if len(before) > 5:
            segments.append({
                "type": "narration", "text": clean_text(before),
                "speaker": "narrator",
                "voice": config.VOICES["narrator"]
            })

        dialogue = quote.group(1).strip()
        after = remaining[quote.end():]
        speaker = "narrator"

        # Check for attribution after: "..." said the Duke
        m1 = re.match(rf'^[,\s\-\–\—]*([A-Z][a-zA-Z\']+)\s+({verb_pattern})', after)
        m2 = re.match(rf'^[,\s\-\–\—]*({verb_pattern})\s+([A-Z][a-zA-Z\']+)', after)

        if m1:
            speaker = normalize_speaker(m1.group(1))
            after = after[m1.end():]
        elif m2:
            speaker = normalize_speaker(m2.group(2))
            after = after[m2.end():]
        
        # Check for attribution before: The Duke said, "..."
        if speaker == "narrator" and before:
            m_before = re.search(rf'([A-Z][a-zA-Z\']+(?:\s+[A-Z][a-zA-Z\']+)?|[Aa] [a-z]+ voice|[Vv]oice)\s+({verb_pattern})', before)
            if m_before:
                speaker = normalize_speaker(m_before.group(1))
            else:
                m_label = re.search(r'([A-Z][a-zA-Z\']+)[:,\s]*$', before)
                if m_label:
                    speaker = normalize_speaker(m_label.group(1))

        voice = config.VOICES.get(speaker, config.VOICES.get("_default", config.VOICES["narrator"]))
        segments.append({
            "type": "dialogue", "text": clean_text(dialogue),
            "speaker": speaker, "voice": voice
        })
        remaining = after

    return segments



def trim_to_target(segments: List[Dict], target_words: int = None) -> List[Dict]:
    """Trim segments to target word count, but preserve intro/outro for branding."""
    if target_words is None:
        target_words = config.TARGET_WORDS

    if not segments:
        return segments
    
    # We no longer strictly preserve intro/outro if they take up 
    # too much time or feel outdated. Let it flow naturally.
    
    # Count words for actual target
    result = []
    count = 0
    for seg in segments:
        w = len(seg['text'].split())
        if count + w > target_words:
            remaining = target_words - count
            if remaining > 10:
                partial = seg.copy()
                partial['text'] = ' '.join(seg['text'].split()[:remaining]) + '...'
                result.append(partial)
            break
        result.append(seg)
        count += w

    return result if result else segments


