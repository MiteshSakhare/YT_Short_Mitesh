def generate_subtitles(
    word_timings: list, output_path: str, total_dur: float
) -> None:
    """
    Generate ASS subtitles.

    UPGRADED:
      • 4 words per cue (vs 3) — fuller lines, less choppy
      • BorderStyle 3 → opaque box behind text (no more floaty text)
      • Semi-transparent dark back colour (AA000000)
      • Karaoke secondary colour = bright cyan (very visible highlight)
      • Proper lower-third positioning (not too close to edge)
      • 1.5px letter spacing for mobile readability
      • Per-character highlight colours preserved

    Fixes problems: subtitle not sync / space not utilized / feels unfinished.
    """
    WORDS_PER_CUE = config.WORDS_PER_CUE         # Punchy and fast, keeps pace with audio
    FONT_SIZE     = config.FONT_SIZE             # Sized properly for the word count
    MARGIN_V      = 550       # 🚀 High margin to avoid YouTube UI buttons
    MARGIN_H      = 50        # horizontal margin so text doesn't stretch full width
    OUTLINE_W     = config.OUTLINE_WIDTH
    LETTER_SPC    = 1.5       # px letter spacing

    # Group words into cues using NATURAL BOUNDARIES (not blind word-count slicing)
    # This prevents awkward mid-sentence breaks like "The Twice-Crowned King A"
    cues = []
    buffer = []

    for wt in word_timings:
        buffer.append(wt)
        word = wt["word"]

        # Check for natural break point: punctuation at end of word
        has_punctuation = any(word.rstrip().endswith(p) for p in ('.', ',', ';', ':', '!', '?', '—', '...'))

        # Decide when to flush the buffer into a cue:
        # Don't break if the current word is a connector/article (orphan prevention)
        is_connector = word.lower().strip('.,!?;:"') in ('a', 'an', 'the', 'of', 'in', 'to', 'for', 'with', 'on', 'at', 'by', 'and', 'but')
        
        should_break = (
            (has_punctuation and len(buffer) >= 2)
            or (len(buffer) >= WORDS_PER_CUE and not is_connector)
            or len(buffer) >= WORDS_PER_CUE + 2  # Force break if too long
        )

        if should_break:
            start_t = buffer[0]["start"]
            end_t = buffer[-1]["end"]

            cues.append({
                "start":   start_t,
                "end":     end_t,
                "words":   buffer,
                "speaker": buffer[0].get("speaker", "narrator"),
            })
            buffer = []

    # Flush remaining words
    if buffer:
        if cues and len(buffer) <= 2:
            # Merge tiny orphan into the last cue (avoids dangling 1-word subtitle)
            cues[-1]["words"].extend(buffer)
            cues[-1]["end"] = buffer[-1]["end"]
        else:
            start_t = buffer[0]["start"]
            end_t = buffer[-1]["end"]
            cues.append({
                "start":   start_t,
                "end":     end_t,
                "words":   buffer,
                "speaker": buffer[0].get("speaker", "narrator"),
            })

    # Fix timing gaps: each cue should stay on screen until the next cue starts
    for idx in range(len(cues) - 1):
        next_start = cues[idx + 1]["start"]
        # Fill the gap up to 1.5 seconds max (avoid lingering during silence)
        cues[idx]["end"] = min(next_start, cues[idx]["end"] + 1.5)

    # Add slight padding for the very last cue, but cap at total_dur
    if cues:
        cues[-1]["end"] = min(total_dur, cues[-1]["end"] + 0.5)

    # ── ASS header ────────────────────────────────────────────
    lines = [
        "[Script Info]",
        "ScriptType: v4.00+",
        "WrapStyle: 0",
        "ScaledBorderAndShadow: yes",
        f"PlayResX: {config.VID_WIDTH}",
        f"PlayResY: {config.VID_HEIGHT}",
        "",
        "[V4+ Styles]",
        "Format: Name, Fontname, Fontsize, PrimaryColour, SecondaryColour, "
        "OutlineColour, BackColour, Bold, Italic, Underline, StrikeOut, "
        "ScaleX, ScaleY, Spacing, Angle, BorderStyle, Outline, Shadow, "
        "Alignment, MarginL, MarginR, MarginV, Encoding",
    ]

    # Character → (primary_colour_spoken, karaoke_secondary_colour_unspoken)
    # Primary is what \kf TURNS INTO. Secondary is what it STARTS AS.
    # ASS colours: &HAABBGGRR  (alpha, blue, green, red in hex)
    CHAR_COLORS = {
        "narrator":   ("&H0000FFFF", "&H00FFFFFF"),  # White → Cyan
        "kaelen":     ("&H002080FF", "&H00FFFFFF"),  # White → Amber
        "seraphina":  ("&H0040FF40", "&H00FFFFFF"),  # White → Green
        "rin":        ("&H00FF80FF", "&H00FFFFFF"),  # White → Pink
        "malachar":   ("&H000040FF", "&H00FFFFFF"),  # White → Red
        "elara":      ("&H00FFCC00", "&H00FFFFFF"),  # White → Blue
        "vex'ahlia":  ("&H00CC44CC", "&H00FFFFFF"),  # White → Purple
        "aldric":     ("&H000088FF", "&H00FFFFFF"),  # White → Gold
        "duke":       ("&H000088FF", "&H00FFFFFF"),  # White → Gold (Duke)
        "valerius":   ("&H000055EE", "&H00FFFFFF"),  # White → Coral
        "gaius":      ("&H0088FFCC", "&H00FFFFFF"),  # White → Teal
        "oracle":     ("&H00FF44FF", "&H00FFFFFF"),  # White → Magenta
        "mara":       ("&H00CCCCCC", "&H00FFFFFF"),  # White → Silver/Cold
        "guard":      ("&H00AAAAAA", "&H00FFFFFF"),  # White → Grey
        "_default":   ("&H0000FFFF", "&H00FFFFFF"),  # White → Cyan
    }

    BACK_COLOR = "&HAA000000"   # Semi-transparent dark box
    OUTLINE    = "&H00000000"   # Black outline

    def _make_style(name: str, primary: str, secondary: str) -> str:
        return (
            f"Style: {name},{config.FONT_NAME},{FONT_SIZE},"
            f"{primary},{secondary},"
            f"{OUTLINE},{BACK_COLOR},"
            f"-1,0,0,0,100,100,{LETTER_SPC},0,3,"  # Bold, Spacing, BorderStyle=3
            f"{OUTLINE_W},0,2,{MARGIN_H},{MARGIN_H},{MARGIN_V},1"
        )

    for char, (pri, sec) in CHAR_COLORS.items():
        style_name = f"S_{char}" if char != "_default" else "Default"
        lines.append(_make_style(style_name, pri, sec))

    lines += [
        "",
        "[Events]",
        "Format: Layer, Start, End, Style, Name, MarginL, MarginR, MarginV, Effect, Text",
    ]

    # Track which styles were defined
    defined_styles = {f"S_{c}" if c != "_default" else "Default" for c in CHAR_COLORS}

    # ── Dialogue events ───────────────────────────────────────
    for cue in cues:
        parts = []
        words = cue["words"]
        for i, w in enumerate(words):
            # Calculate duration to the START of the next word, or the end of the cue
            if i < len(words) - 1:
                next_start = words[i+1]["start"]
                dur = next_start - w["start"]
            else:
                dur = w["end"] - w["start"]
            
            dur_cs = max(1, int(dur * 100))
            word   = (w["word"]
                      .replace("{", "").replace("}", "")
                      .replace("\\", "").replace("\n", " ")
                      .replace("/n", " ").replace("/N", " "))
            
            # Final sweep for any remaining emojis before subtitle rendering
            word = re.sub(r'[\U0001f300-\U0001f650\U0001f680-\U0001f6A0\U0001f900-\U0001f9FF\u2600-\u26FF\u2700-\u27BF]', '', word)
            
            # Since ASS V4+ Karaoke \kf changes from SecondaryColour to PrimaryColour:
            # We want it to be White (unspoken) and turn Cyan (spoken).
            # So the style is Primary=Cyan, Secondary=White.
            # \kf smoothly transitions them.
            # Avoid using \fscx animations here as they affect all remaining text on the line,
            # causing severe stutter/jitter.
            pop_tag = f"{{\\kf{dur_cs}}}"
            parts.append(pop_tag + word)

        text    = " ".join(parts)
        speaker = cue.get("speaker", "narrator")

        style_key = f"S_{speaker}" if f"S_{speaker}" in defined_styles else "Default"

        lines.append(
            f"Dialogue: 0,{sec_to_ass(cue['start'])},{sec_to_ass(cue['end'])},"
            f"{style_key},,0,0,0,,{text}"
        )

    with open(output_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    logger.info(f"✅ Subtitles — {len(cues)} cues ({WORDS_PER_CUE}w/cue) → {output_path}")


def generate_hook(segments: List[Dict], part_num: int, output_png: str) -> str:
    """
    Generate hook text using psychology engine and create overlay image.
    """
    # Check for custom hook override
    hook_text = getattr(config, 'HOOKS', {}).get(part_num)
    
    if not hook_text:
        # Use psychology engine to select the best hook from the script
        raw_hook = choose_hook_sentence(segments)
        hook_text = build_open_loop_hook(raw_hook, part_num)

    # Create hook image
    img = Image.new("RGBA", (config.VID_WIDTH, config.VID_HEIGHT), (0, 0, 0, 0))
    draw = ImageDraw.Draw(img)

    # Try fonts
    fp = None
    font = sm_font = None
    for fp in [
        "/usr/share/fonts/truetype/bangers.ttf",
        "/usr/share/fonts/truetype/liberation/LiberationSans-Bold.ttf",
        "C:\\Windows\\Fonts\\impact.ttf",
        "C:\\Windows\\Fonts\\arialbd.ttf",
        "C:\\Windows\\Fonts\\arial.ttf",
    ]:
        if os.path.exists(fp):
            try:
                font = ImageFont.truetype(fp, 75)
                sm_font = ImageFont.truetype(fp, 40)
                break
            except Exception:
                pass

    if font is None:
        font = sm_font = ImageFont.load_default()

    hook_lines = hook_text.strip().split('\n')
    
    # Auto-scale font size based on text length to prevent overflow
    avg_line_length = sum(len(line) for line in hook_lines) / len(hook_lines) if hook_lines else 0
    if avg_line_length > 35:  # Long text - reduce font size
        font = ImageFont.truetype(fp, 55) if 'fp' in locals() and os.path.exists(fp) else font
        line_height = 75
    elif avg_line_length > 25:  # Medium text
        font = ImageFont.truetype(fp, 65) if 'fp' in locals() and os.path.exists(fp) else font
        line_height = 90
    else:  # Short text - can use larger font
        font = ImageFont.truetype(fp, 80) if 'fp' in locals() and os.path.exists(fp) else font
        line_height = 105

    # Panel
    panel_h = len(hook_lines) * line_height + 50
    panel_y = config.VID_HEIGHT // 3 - panel_h // 2

    # Semi-transparent background
    draw.rectangle(
        [40, panel_y - 25, config.VID_WIDTH - 40, panel_y + panel_h],
        fill=(0, 0, 0, 170)
    )

    # Draw text with outline
    for i, line in enumerate(hook_lines):
        y = panel_y + 10 + i * line_height

        # Outline
        for dx, dy in [(-4,0),(4,0),(0,-4),(0,4),(-3,-3),(3,-3),(-3,3),(3,3)]:
            draw.text(
                (config.VID_WIDTH // 2 + dx, y + dy),
                line, font=font, fill=(0, 0, 0, 255), anchor="mt"
            )

        # Main text (golden yellow)
        draw.text(
            (config.VID_WIDTH // 2, y),
            line, font=font, fill=(255, 215, 0, 255), anchor="mt"
        )

    # Part badge
    if sm_font:
        draw.text(
            (config.VID_WIDTH // 2, panel_y - 50),
            f"PART {part_num}",
            font=sm_font, fill=(180, 180, 180, 200), anchor="mt"
        )

    img.save(output_png)
    logger.info(f"   🪝 Hook saved → {output_png}")
    return hook_text


def _auto_generate_hook(segments) -> str:
    """Extract a viral hook from the first dialogue or narration."""
    for seg in segments:
        if seg['type'] == 'dialogue' or seg['type'] == 'narration':
            text = seg['text'].strip()
            # Clean up and shorten
            text = re.sub(r'\(.*?\)', '', text)  # Remove bracketed directions
            words = text.split()
            if len(words) > 8:
                return " ".join(words[:7]) + "!"
            return text
    return "The Twice-Crowned King!"

