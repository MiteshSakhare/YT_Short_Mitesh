def _get_thumbnail_bg(mood: str) -> str:
    """Fetch or choose a background image."""
    search_terms = config.PEXELS_SEARCH_TERMS.get(mood, ["dark fantasy landscape", "epic castle", "mysterious forest"])
    query = random.choice(search_terms) + " cinematic 4k"
    
    cache_dir = Path(".cache/thumbnails")
    cache_dir.mkdir(parents=True, exist_ok=True)
    cache_hash = hashlib.md5(query.encode()).hexdigest()[:12]
    cached_bg = cache_dir / f"bg_{cache_hash}.jpg"
    
    if cached_bg.exists():
        return str(cached_bg)
        
    if not config.PEXELS_API_KEY:
        return None

    try:
        headers = {"Authorization": config.PEXELS_API_KEY}
        url = f"https://api.pexels.com/v1/search?query={query}&per_page=5&orientation=portrait"
        resp = requests.get(url, headers=headers, timeout=10)
        data = resp.json()
        
        if data.get("photos"):
            photo = random.choice(data["photos"])
            img_url = photo["src"]["large2x"]
            img_data = requests.get(img_url, timeout=10).content
            with open(cached_bg, "wb") as f:
                f.write(img_data)
            return str(cached_bg)
    except Exception as e:
        logger.error(f"Pexels image search failed: {e}")
        
    return None

def _smart_resize_crop(img, target_w, target_h):
    """Resize and center crop to fill target dimensions."""
    aspect = target_w / target_h
    img_aspect = img.width / img.height
    
    if img_aspect > aspect:
        # Image is wider than target
        new_h = target_h
        new_w = int(target_h * img_aspect)
        img = img.resize((new_w, new_h), Image.LANCZOS)
        left = (new_w - target_w) // 2
        img = img.crop((left, 0, left + target_w, target_h))
    else:
        # Image is taller than target
        new_w = target_w
        new_h = int(target_w / img_aspect)
        img = img.resize((new_w, new_h), Image.LANCZOS)
        top = (new_h - target_h) // 2
        img = img.crop((0, top, target_w, top + target_h))
        
    return img

def _apply_cinematic_filters(img):
    """Add vignette and bottom gradient for text readability."""
    # Bottom Gradient
    overlay = Image.new("RGBA", img.size, (0, 0, 0, 0))
    draw = ImageDraw.Draw(overlay)
    
    # Create a nice dark gradient at the bottom
    grad_height = int(img.height * 0.4)
    opacity = getattr(config, "THUMBNAIL_OVERLAY_OPACITY", 180)
    for i in range(grad_height):
        alpha = int((i / grad_height) * opacity)
        draw.line([(0, img.height - i), (img.width, img.height - i)], fill=(0, 0, 0, alpha))
    
    return Image.alpha_composite(img.convert("RGBA"), overlay).convert("RGB")

def _draw_hook_text(draw, text, part_num):
    """Draw high-impact clickbait text."""
    font_path = config.get_font_path("impact")
    
    # Process text for thumbnail (keep it short)
    words = text.replace('"', '').split()
    hook = " ".join(words[:6]).upper()
    if len(words) > 6: hook += "..."
    
    # Load fonts
    try:
        font_main = ImageFont.truetype(font_path, config.THUMBNAIL_FONT_SIZE)
        font_part = ImageFont.truetype(font_path, 80)
    except (OSError, IOError):
        font_main = ImageFont.load_default()
        font_part = ImageFont.load_default()
        
    # Draw "PART X" tag
    part_text = f"PART {part_num}"
    draw.text((50, 100), part_text, font=font_part, fill=(255, 255, 255, 255), stroke_width=4, stroke_fill=(0, 0, 0))
    
    # Wrap text for the hook
    max_w = config.VID_WIDTH - 100
    lines = _wrap_thumbnail_text(hook, font_main, max_w)
    
    # Draw from bottom up
    total_h = len(lines) * (config.THUMBNAIL_FONT_SIZE + 20)
    curr_y = config.VID_HEIGHT - total_h - 150
    
    for line in lines:
        # Get text size
        bbox = draw.textbbox((0, 0), line, font=font_main)
        w = bbox[2] - bbox[0]
        x = (config.VID_WIDTH - w) // 2
        
        # Outline
        draw.text((x, curr_y), line, font=font_main, fill=config.THUMBNAIL_ACCENT_COLOR, 
                  stroke_width=config.THUMBNAIL_OUTLINE_WIDTH, stroke_fill=(0, 0, 0))
        curr_y += config.THUMBNAIL_FONT_SIZE + 20

def _wrap_thumbnail_text(text, font, max_width):
    lines = []
    words = text.split()
    while words:
        line = ""
        while words and font.getlength(line + words[0]) < max_width:
            line += words.pop(0) + " "
        
        if not line and words:
            line = words.pop(0) + " "
            
        lines.append(line.strip())
    return lines


