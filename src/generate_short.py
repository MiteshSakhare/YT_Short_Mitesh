def _add_emotional_breaks(text: str, character: str) -> str:
    """
    Inject natural pause cues into TTS text.
    Edge-TTS reads double-spaces and ellipses as breath pauses.

    Fixes: Robotic delivery with no natural pacing.
    """
    # Longer breath after full stops that begin a new sentence
    text = re.sub(r'\. ([A-Z])', r'.  \1', text)

    # Suspense pause after question marks
    text = re.sub(r'\? ([A-Z])', r'?  \1', text)

    # Sharp dramatic pause after exclamation
    text = re.sub(r'! ([A-Z])', r'!  \1', text)

    # Beat after em-dash (narrative hesitation)
    text = re.sub(r'— ', r'—  ', text)

    # Extend existing ellipsis pauses
    text = re.sub(r'\.\.\. ', r'...  ', text)

    # Dialogue attribution tag removal (e.g. ", said Kaelen" — don't read the tag)
    # These get stripped upstream but just in case
    text = re.sub(r',?\s+(said|whispered|replied|murmured|growled|shouted)\s+\w+\.?$',
                  '', text, flags=re.IGNORECASE)

    return text.strip()


def _get_emotional_prosody(character: str, text: str, segment_position: float, mood: str = "neutral") -> tuple:
    """
    Compute rate/pitch for a segment based on character + dramatic position.

    segment_position: 0.0 = first segment of the video, 1.0 = last segment.

    Dramatic arc:
      0.0–0.2  → slow + low   (mysterious opening)
      0.2–0.7  → neutral      (story build)
      0.7–1.0  → faster/higher (climax / emotional peak)

    Fixes: Single flat tone throughout entire video.
    """
    style = config.VOICE_STYLE.get(character, config.VOICE_STYLE['_default'])

    # Parse base integers from "+0%" and "+0Hz" strings
    base_rate_raw  = style.get('rate',  '+0%')
    base_pitch_raw = style.get('pitch', '+0Hz')

    try:
        base_rate  = int(re.findall(r'[+-]?\d+', base_rate_raw)[0])
        base_pitch = int(re.findall(r'[+-]?\d+', base_pitch_raw)[0])
    except (IndexError, ValueError):
        base_rate, base_pitch = 0, 0

    # Emotional keyword detection (expanded for "Twice-Crowned King")
    intense = {
        'screamed', 'shouted', 'roared', 'impossible', 'never', 'fury', 'rage',
        'wept', 'tears', 'gasped', 'desperate', 'betrayed', 'lied', 'truth',
        'final', 'last', 'death', 'blood', 'kill', 'betrayal', 'void', 'darkness',
        'power', 'unbelievable', 'surrender', 'monster', 'demon', 'emperor',
        'vengeance', 'curse', 'die', 'suffering', 'wrath', 'unyielding'
    }
    calm = {
        'whispered', 'murmured', 'quietly', 'softly', 'gentle', 'silent',
        'still', 'peaceful', 'slowly', 'sighed', 'breathed', 'calm', 'patience',
        'wait', 'hidden', 'secret', 'shadows', 'mystery', 'ancient', 'cold',
        'winter', 'frozen', 'sleep', 'dream', 'reflection', 'noble'
    }

    words_lower = set(re.findall(r'\w+', text.lower()))
    is_intense = bool(words_lower & intense)
    is_calm = bool(words_lower & calm)

    # Narrative arc modifier
    if segment_position < 0.15:       # Hook / Opening - slow and mysterious
        arc_rate, arc_pitch = -3, -2
    elif segment_position > 0.85:     # Climax / Outro - fast and impactful
        arc_rate, arc_pitch = +5, +3
    else:                              # Build
        arc_rate, arc_pitch = 0, 0

    # Mood modifier (Passed from segment analysis)
    mood_mod_rate, mood_mod_pitch = 0, 0
    m = mood.lower()
    if m == "epic":         mood_mod_rate, mood_mod_pitch = +2, +1
    elif m == "dark":       mood_mod_rate, mood_mod_pitch = -1, -2
    elif m == "thrill":     mood_mod_rate, mood_mod_pitch = +4, +2
    elif m == "sad":        mood_mod_rate, mood_mod_pitch = -3, -2
    elif m == "mysterious": mood_mod_rate, mood_mod_pitch = -2, -1
    elif m == "happy":      mood_mod_rate, mood_mod_pitch = +1, +2

    # Keyword modifier (Aggressive shift)
    kw_rate, kw_pitch = 0, 0
    if is_intense:
        kw_rate, kw_pitch = +4, +3
    elif is_calm:
        kw_rate, kw_pitch = -3, -2

    # Combine all modifiers
    total_rate_mod = arc_rate + mood_mod_rate + kw_rate
    total_pitch_mod = arc_pitch + mood_mod_pitch + kw_pitch

    # Clamp to edge-tts valid range
    final_rate = max(-35, min(40, base_rate + total_rate_mod))
    final_pitch = max(-20, min(20, base_pitch + total_pitch_mod))

    rate_str  = f"{'+' if final_rate  >= 0 else ''}{final_rate}%"
    pitch_str = f"{'+' if final_pitch >= 0 else ''}{final_pitch}Hz"

    return rate_str, pitch_str


# ────────────────────────────────────────────────────────────────
#  REPLACE generate_tts_segment()
#  New args: character, segment_position (both have defaults → backward-compat)
# ────────────────────────────────────────────────────────────────


async def generate_tts_segment(
    text: str,
    voice: str,
    rate: str,
    pitch: str,
    output_path: str,
    character: str = "_default",
    segment_position: float = 0.5,
    mood: str = "neutral",
) -> list:
    """
    Generate TTS audio + word-level timing for one segment.

    UPGRADED:
      • Natural pause injection at punctuation
      • Emotion-aware rate/pitch based on story position
      • Local TTS Integration (Kokoro / XTTSv2)
    """
    # Strip emojis and newlines so the TTS engine does not read them aloud (redundant but safe)
    text = text.replace('\\N', ' ').replace('\\n', ' ')
    text = text.strip()

    # Inject natural breathing pauses
    processed = _add_emotional_breaks(text, character)

    # Compute emotion-aware prosody (overrides static config)
    emo_rate, emo_pitch = _get_emotional_prosody(character, text, segment_position, mood=mood)

    # ── TTS CACHE LOGIC ───────────────────────────────────────
    cache_enabled = getattr(config, 'TTS_CACHE_ENABLED', True)
    if cache_enabled:
        cache_dir = Path(".cache/tts")
        cache_dir.mkdir(parents=True, exist_ok=True)
        
        # Hash text, voice, and prosody to uniquely identify this audio
        cache_payload = f"{processed}|{voice}|{emo_rate}|{emo_pitch}".encode('utf-8')
        cache_hash = hashlib.md5(cache_payload).hexdigest()
        
        audio_cache = cache_dir / f"{cache_hash}.wav"
        timing_cache = cache_dir / f"{cache_hash}.json"
        
        if audio_cache.exists() and timing_cache.exists():
            logger.info(f"   ✨ TTS Cache Hit: {character} (hash={cache_hash[:8]})")
            shutil.copy(audio_cache, output_path)
            try:
                with open(timing_cache, 'r', encoding='utf-8') as f:
                    return json.load(f)
            except (json.JSONDecodeError, OSError):
                logger.warning("   ⚠️ TTS Timing cache corrupt, regenerating...")
    # ──────────────────────────────────────────────────────────

    # Use local Kokoro-TTS if configured, otherwise fallback to Edge-TTS
    if getattr(config, "USE_LOCAL_TTS", False):
        try:
            try:
                # Works when running as module: `python -m src.generate_short ...`
                from src.local_tts import generate_kokoro_audio
            except ImportError:
                # Works when running as script: `python src/generate_short.py ...`
                from local_tts import generate_kokoro_audio
            # Map edge-tts rates like +10% to kokoro floats like 1.1
            speed = 1.0
            if emo_rate.endswith('%'):
                speed += float(emo_rate.strip('%')) / 100.0
                
            if character.lower() == "narrator":
                kokoro_voice = getattr(config, "KOKORO_VOICES", {}).get("narrator", "af_bella")
            else:
                kokoro_voice = getattr(config, "KOKORO_VOICES", {}).get(character, getattr(config, "KOKORO_VOICES", {}).get("_default", "af_bella"))

            logger.info(f"   🎤 Using KOKORO TTS for {character} (voice={kokoro_voice}, speed={speed:.1f}, mood={mood})")
            result = generate_kokoro_audio(
                text=processed,
                voice=kokoro_voice,
                speed=max(0.5, min(2.0, speed)),
                output_path=output_path
            )
            logger.info(f"   ✅ Kokoro TTS success for {character}")
            return result
        except Exception as e:
            logger.warning(f"⚠️ Kokoro TTS FAILED for {character}: {e}. Falling back to Edge-TTS.")

    logger.info(f"   🔊 Using Edge-TTS for {character} (voice={voice})")

    # Edge-TTS fallback
    communicate = edge_tts.Communicate(processed, voice, rate=emo_rate, pitch=emo_pitch)
    timings = []

    with open(output_path, 'wb') as f:
        async for chunk in communicate.stream():
            if chunk["type"] == "audio":
                # FFmpeg concat handles rate conversion, but just in case
                f.write(chunk["data"])
            elif chunk["type"] == "WordBoundary":
                timings.append({
                    "word":  chunk["text"],
                    "start": chunk["offset"] / 10_000_000,
                    "end":   (chunk["offset"] + chunk["duration"]) / 10_000_000,
                })

    # Save to cache if enabled
    if cache_enabled:
        shutil.copy(output_path, audio_cache)
        with open(timing_cache, 'w', encoding='utf-8') as f:
            json.dump(timings, f)

    return timings


# ────────────────────────────────────────────────────────────────
#  REPLACE generate_all_audio()
#  Adds segment_position calculation and passes it through
# ────────────────────────────────────────────────────────────────


async def generate_all_audio(
    segments: list, audio_dir
) -> tuple:
    """
    Generate TTS for all segments with parallel execution.

    UPGRADED: Passes segment_position (0.0–1.0) to each TTS call
    so prosody creates a natural dramatic arc over the video.

    Returns: (combined_audio_path, word_timings, total_duration)
    """
    logger.info(f"🎙  Generating TTS for {len(segments)} segments (PARALLEL + EMOTION)…")

    total_segs = max(len(segments), 1)

    async def generate_segment_tts(seg_idx: int, seg: dict) -> tuple:
        """Generate TTS for one segment and return (index, path, timings)."""
        style    = config.VOICE_STYLE.get(seg['speaker'], config.VOICE_STYLE['_default'])
        raw_path = str(audio_dir / f"seg_{seg_idx:04d}_raw.wav")

        # Dramatic position: 0.0 = first, 1.0 = last
        position = seg_idx / (total_segs - 1) if total_segs > 1 else 0.5

        try:
            timings = await generate_tts_segment(
                seg['text'], seg['voice'],
                style['rate'], style['pitch'],
                raw_path,
                character=seg['speaker'],
                segment_position=position,
                mood=seg.get('mood', 'neutral'),
            )
            return (seg_idx, raw_path, timings)

        except Exception as e:
            logger.warning(f"⚠️  Segment {seg_idx} TTS failed ({e}), retrying default…")
            try:
                timings = await generate_tts_segment(
                    seg['text'], config.VOICES['_default'],
                    "+0%", "+0Hz", raw_path,
                )
                return (seg_idx, raw_path, timings)
            except Exception as e2:
                logger.error(f"❌ Segment {seg_idx} completely failed: {e2}")
                return (seg_idx, None, [])

    # Parallel generation
    tts_tasks   = [generate_segment_tts(i, seg) for i, seg in enumerate(segments)]
    tts_results = await asyncio.gather(*tts_tasks)
    logger.info("✅ All TTS generated in parallel with emotional arc")

    # Sequential post-processing
    all_timings = []
    audio_files = []
    offset      = 0.0

    for seg_idx, raw_path, timings in tts_results:
        if raw_path is None or not os.path.exists(raw_path) or os.path.getsize(raw_path) < 100:
            logger.warning(f"⚠️  Segment {seg_idx}: No valid audio")
            continue

        fx_path    = str(audio_dir / f"seg_{seg_idx:04d}_fx.wav")
        seg        = segments[seg_idx]
        processed_path = apply_character_fx(raw_path, fx_path, seg['speaker'])
        
        # Apply mood-based EQ shaping
        final_path = str(audio_dir / f"seg_{seg_idx:04d}.wav")
        final_path = apply_mood_shaping(processed_path, final_path, seg.get('mood', 'neutral'))
        
        dur = get_audio_duration(final_path)
        
        # Store segment boundaries
        seg['start'] = offset
        seg['end']   = seg['start'] + dur
        seg['duration'] = dur

        # Word boundary fallback (even word spacing)
        if not timings:
            words    = seg['text'].split()
            word_dur = dur / max(len(words), 1)
            for w_idx, w in enumerate(words):
                timings.append({
                    "word":  w,
                    "start": w_idx * word_dur,
                    "end":   (w_idx + 1) * word_dur,
                })

        for t in timings:
            all_timings.append({
                "word":    t["word"],
                "start":   t["start"] + offset,
                "end":     t["end"]   + offset,
                "speaker": seg["speaker"],
                "type":    seg["type"],
            })

        offset += dur
        audio_files.append(final_path)

    if not audio_files:
        raise RuntimeError("No audio segments generated — check script format")

    # Concatenate
    combined    = str(audio_dir.parent / "combined_audio.wav")
    concat_file = str(audio_dir / "concat.txt")

    with open(concat_file, 'w') as f:
        for af in audio_files:
            f.write(f"file '{af}'\n")

    subprocess.run(
        ["ffmpeg", "-y", "-f", "concat", "-safe", "0", "-i", concat_file, "-af", "anull,apad=pad_dur=0.1", combined],
        check=True, capture_output=True
    )

    normalized = str(audio_dir.parent / "combined_normalized.wav")
    normalize_audio(combined, normalized)
    if os.path.exists(normalized) and os.path.getsize(normalized) > 100:
        combined = normalized

    total_dur = get_audio_duration(combined)
    logger.info(f"✅ Audio ready — {total_dur:.1f}s | {len(audio_files)} segments")
    return combined, all_timings, total_dur


