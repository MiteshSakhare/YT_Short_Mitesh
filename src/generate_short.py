def add_loop_bridge(audio_path: str, output_path: str) -> str:
    """Echo opening audio at the end → triggers replays."""
    bridge = config.LOOP_BRIDGE_DURATION
    logger.info(f"   🔁 Adding loop bridge ({bridge}s)…")

    try:
        # Extract first N seconds with fade in/out
        bridge_temp = str(Path(output_path).parent / "bridge_temp.mp3")
        cmd = [
            "ffmpeg", "-y", "-i", audio_path,
            "-t", str(bridge),
            "-af", f"afade=t=in:st=0:d=0.3,afade=t=out:st={max(0.1, bridge-0.5)}:d=0.5",
            "-c:a", "libmp3lame", bridge_temp
        ]
        
        r = subprocess.run(cmd, capture_output=True, text=True, timeout=30)
        if r.returncode != 0:
            raise Exception(f"Bridge extraction failed: {r.stderr}")

        # Concatenate original + bridge
        concat_file = Path(output_path).parent / "concat_list.txt"
        audio_path_unix = Path(audio_path).absolute().as_posix()
        bridge_temp_unix = Path(bridge_temp).absolute().as_posix()
        with open(concat_file, 'w') as f:
            f.write(f"file '{audio_path_unix}'\n")
            f.write(f"file '{bridge_temp_unix}'\n")

        cmd = [
            "ffmpeg", "-y", "-f", "concat", "-safe", "0",
            "-i", str(concat_file),
            "-c:a", "libmp3lame", "-b:a", "192k", output_path
        ]

        r = subprocess.run(cmd, capture_output=True, text=True, timeout=30)
        if r.returncode != 0:
            raise Exception(f"Concatenation failed: {r.stderr}")

        # Cleanup temp files
        Path(concat_file).unlink(missing_ok=True)
        Path(bridge_temp).unlink(missing_ok=True)

        logger.info("   ✅ Loop bridge added")
        return output_path

    except Exception as e:
        logger.warning(f"⚠️  Loop bridge failed ({str(e)[:50]}), using original")
        shutil.copy(audio_path, output_path)
        return output_path


# ═══════════════════════════════════════════════════════════════════════════════
#  SFX TIMELINE MIXING (Fix 3: Actually place SFX on the audio timeline)
# ═══════════════════════════════════════════════════════════════════════════════

def mix_sfx_into_audio(
    voice_path: str,
    mood_sfx: Dict,
    segments: List[Dict],
    total_dur: float,
    output_path: str,
) -> str:
    """
    Layer mood-based SFX onto the voice audio at appropriate timeline positions.

    Strategy:
      - Place transition SFX (whoosh) at mood-change boundaries
      - Place accent SFX (boom, whisper) at first segment of each mood group
      - Mix at 30% volume so SFX enhances but doesn't overpower voice
    """
    if not mood_sfx:
        logger.info("   ⏭  No SFX available for this mood — skipping SFX mix")
        return voice_path

    # Find SFX files to use
    sfx_files = []
    for sfx_type, sfx_path in mood_sfx.items():
        if os.path.exists(sfx_path):
            sfx_files.append((sfx_type, sfx_path))

    if not sfx_files:
        logger.info("   ⏭  No SFX files found on disk — skipping SFX mix")
        return voice_path

    # Strategy: Place SFX at mood change boundaries for narrative flow
    placements = []
    current_mood = segments[0].get('mood', 'neutral')
    
    for i in range(1, len(segments)):
        seg_mood = segments[i].get('mood', 'neutral')
        if seg_mood != current_mood:
            # Mood changed! Place a transition SFX
            ts = segments[i]['start']
            # Cycle through available SFX
            sfx_type, sfx_path = sfx_files[len(placements) % len(sfx_files)]
            placements.append((ts, sfx_path, sfx_type))
            current_mood = seg_mood

    # Ensure at least one SFX if none were triggered by mood changes
    if not placements and sfx_files:
        placements.append((total_dur * 0.4, sfx_files[0][1], sfx_files[0][0]))

    if not placements:
        return voice_path

    # Build FFmpeg command to layer SFX onto voice audio
    inputs = ["-i", voice_path]
    filter_parts = []

    for idx, (ts, sfx_path, sfx_type) in enumerate(placements):
        inputs += ["-i", sfx_path]
        input_idx = idx + 1
        # Delay each SFX to its timestamp
        delay_ms = int(ts * 1000)
        sfx_vol = getattr(config, 'SFX_VOLUME', 0.30)
        filter_parts.append(
            f"[{input_idx}:a]volume={sfx_vol},adelay={delay_ms}|{delay_ms}[sfx{idx}]"
        )

    # Fix: amix divides each input's volume by 1/N. Even with normalize=0, the voice
    # gets attenuated when multiple SFX are mixed. Pre-boost voice to compensate.
    sfx_labels = "".join(f"[sfx{i}]" for i in range(len(placements)))
    mix_inputs = len(placements) + 1  # voice + all SFX
    filter_parts.append(
        f"[0:a]volume={mix_inputs}[v_boosted]"
    )
    filter_parts.append(
        f"[v_boosted]{sfx_labels}amix=inputs={mix_inputs}:duration=first:dropout_transition=0:normalize=0[out]"
    )

    filter_complex = "; ".join(filter_parts)

    cmd = ["ffmpeg", "-y"] + inputs + [
        "-filter_complex", filter_complex,
        "-map", "[out]",
        "-c:a", "pcm_s16le",
        "-ar", config.AUDIO_SAMPLE_RATE,
        output_path,
    ]

    try:
        r = subprocess.run(cmd, capture_output=True, text=True, timeout=120)
        if r.returncode == 0:
            logger.info(f"   ✅ SFX mixed — {len(placements)} effects placed on timeline")
            return output_path
        else:
            logger.warning(f"⚠️  SFX mixing failed: {r.stderr[:200]}")
            return voice_path
    except subprocess.TimeoutExpired:
        logger.warning("⚠️  SFX mixing timed out — using voice-only audio")
        return voice_path


# ═══════════════════════════════════════════════════════════════════════════════
#  VIDEO COMPOSITION — 2-PASS PIPELINE (Fix 6)
#
#  Pass 1: Video + Visual Overlays (subs, hook, watermark, CTA, progress bar)
#    → intermediate.mp4  (no audio)
#
#  Pass 2: Audio Mix (voice + music with ducking) → merge with video
#    → final.mp4  (uses -c:v copy = no re-encode = 10x faster)
#
#  Benefits:
#    • Each pass is independently debuggable
#    • Filter graph strings are much shorter (no Windows cmd limit issues)
#    • Pass 2 is nearly instant (no video re-encoding)
# ═══════════════════════════════════════════════════════════════════════════════

def compose_video(
    bg_video: str, audio: str, subtitles: str, hook_img: str,
    music: str, output: str, duration: float, part_num: int = 1
) -> None:
    """
    Assemble the final YouTube Short using a single-pass pipeline.
    All video layers, subtitles, overlays and audio are composited in one FFmpeg call.
    """
    logger.info("🎬 Composing final Short (single-pass pipeline)…")

    final_dur = duration
    subs_esc = subtitles.replace("\\", "/").replace(":", "\\:")

    # ── SINGLE-PASS COMPOSITION: VIDEO + AUDIO + OVERLAYS ─────────────────
    logger.info("   🎬 Composing final video (Single Pass — Deep Sync)...")

    # Inputs preparation
    inputs_args = []
    input_cursor = 0

    # 0. Background Video (Looped for safety)
    inputs_args += ["-stream_loop", "-1", "-i", bg_video]
    bg_idx = input_cursor
    input_cursor += 1
    
    # 1. Hook image (if exists)
    has_hook = config.SHOW_HOOK and os.path.exists(hook_img)
    hook_idx = -1
    if has_hook:
        inputs_args += ["-i", hook_img]
        hook_idx = input_cursor
        input_cursor += 1

    # 2. Watermark image (if exists)
    has_watermark = (
        config.SHOW_CHANNEL_WATERMARK
        and hasattr(config, 'WATERMARK_IMAGE')
        and os.path.exists(str(config.WATERMARK_IMAGE))
    )
    watermark_idx = -1
    if has_watermark:
        inputs_args += ["-i", str(config.WATERMARK_IMAGE)]
        watermark_idx = input_cursor
        input_cursor += 1

    # 3. Main Audio (REQUIRED)
    audio_idx = input_cursor
    inputs_args += ["-i", audio]
    input_cursor += 1

    # 4. Background Music (if exists)
    has_music = music and os.path.exists(music)
    music_idx = -1
    if has_music:
        inputs_args += ["-i", music]
        music_idx = input_cursor
        input_cursor += 1


    # ── FILTER GRAPH: VIDEO ───────────────────────────────────────────
    current_v = "[0:v]"
    v_parts = []

    # Layer 1: Hook overlay
    if has_hook:
        v_parts.append(
            f"[{hook_idx}:v]scale={config.VID_WIDTH}:{config.VID_HEIGHT}:"
            f"force_original_aspect_ratio=increase,"
            f"crop={config.VID_WIDTH}:{config.VID_HEIGHT},"
            f"fade=t=out:st=4.7:d=0.3[hookv]"
        )
        v_parts.append(
            f"{current_v}[hookv]overlay=0:0:enable='lte(t,5.0)'[with_hook]"
        )
        current_v = "[with_hook]"

    # Layer 2: Subtitles (ASS)
    v_parts.append(f"{current_v}ass='{subs_esc}'[subbed]")
    current_v = "[subbed]"

    # Layer 3: Watermark
    if has_watermark:
        margin = getattr(config, 'WATERMARK_MARGIN', 30)
        scale_w = getattr(config, 'WATERMARK_SCALE', 120)
        opacity = getattr(config, 'WATERMARK_OPACITY', 0.70)
        pos = getattr(config, 'WATERMARK_POSITION', 'top-left')
        
        if pos == "top-left": x_wm, y_wm = margin, margin
        elif pos == "top-right": x_wm, y_wm = f"W-w-{margin}", margin
        else: x_wm, y_wm = margin, f"H-h-{margin}"

        v_parts.append(
            f"[{watermark_idx}:v]scale={scale_w}:-1,"
            f"format=rgba,colorchannelmixer=aa={opacity}[wm]"
        )
        v_parts.append(
            f"{current_v}[wm]overlay={x_wm}:{y_wm}[watermarked]"
        )
        current_v = "[watermarked]"

    # Layer 4: Part tag
    if getattr(config, 'SHOW_PART_TAG', False):
        pt_dur = getattr(config, 'PART_TAG_DURATION', 5.0)
        v_parts.append(
            f"{current_v}drawtext="
            f"fontfile='{_win_font}':text='PART {part_num}':"
            f"fontsize=64:fontcolor=white@0.95:borderw=4:bordercolor=black@0.8:"
            f"x=w-tw-40:y=80:enable='lte(t,{pt_dur})'[pt]"
        )
        current_v = "[pt]"

    # Layer 5: CTA overlay
    if getattr(config, 'SHOW_CTA_OVERLAY', False):
        cta_dur = getattr(config, 'CTA_DURATION', 4.0)
        cta_raw = config.CTA_TEXT.format(next_part=part_num + 1)
        cta_text = cta_raw.replace("'", "\u2019")
        cta_start = max(0, final_dur - cta_dur)
        v_parts.append(
            f"{current_v}drawtext="
            f"fontfile='{_win_font}':text='{cta_text}':"
            f"fontsize=64:fontcolor=yellow@0.98:borderw=4:bordercolor=black@0.9:"
            f"x=(w-tw)/2:y=h*0.15:enable='gte(t,{cta_start})'[cta]"
        )
        current_v = "[cta]"

    # Layer 6: Final Formatting (No custom progress bar, rely on native UI)
    v_parts.append(
        f"{current_v}fps={config.VID_FPS},"
        f"setpts=PTS-STARTPTS,"
        f"setsar=1/1,"
        f"format=yuv420p[final_v]"
    )

    # ── FILTER GRAPH: AUDIO ───────────────────────────────────────────
    if has_music:
        mv = str(getattr(config, 'MUSIC_VOLUME', 0.10))
        # 1. Target -14 LUFS for the master broadcast volume of the TTS
        v_parts.append(f"[{audio_idx}:a]loudnorm=I=-14:TP=-1.5:LRA=11[voice]")
        v_parts.append(f"[{music_idx}:a]volume={mv}[bgm]")
        v_parts.append(f"[voice]asplit[v1][v2]")
        # 2. Sidechaincompress squash the background music when TTS speaks
        v_parts.append(f"[bgm][v2]sidechaincompress=threshold=-20dB:ratio=6:attack=50:release=300:level_sc=0.8[ducked_bgm]")
        v_parts.append(f"[v1][ducked_bgm]amix=inputs=2:duration=first:dropout_transition=2[final_a]")
        a_map = "[final_a]"
    else:
        v_parts.append(f"[{audio_idx}:a]loudnorm=I=-14:TP=-1.5:LRA=11[final_a]")
        a_map = "[final_a]"

    full_fg = "; ".join(v_parts)

    # ── FINAL COMMAND ────────────────────────────────────────────────
    cmd = [
        "ffmpeg", "-y",
        "-fflags", "+genpts",
        "-avoid_negative_ts", "make_zero",
    ] + inputs_args + [

        "-filter_complex", full_fg,
        "-map", "[final_v]",
        "-map", a_map,
        "-t", f"{final_dur:.3f}",
        "-r", str(config.VID_FPS),
        "-vsync", "cfr",
        "-c:v", config.VIDEO_CODEC,
        "-preset", config.VIDEO_PRESET,
        "-crf", str(config.VIDEO_CRF),
        "-c:a", "aac", "-b:a", config.AUDIO_BITRATE, "-ar", config.AUDIO_SAMPLE_RATE,
        "-movflags", "+faststart",
        "-pix_fmt", "yuv420p",
        output
    ]

    try:
        r = subprocess.run(cmd, capture_output=True, text=True, timeout=900)
        if r.returncode != 0:
            logger.error(f"❌ Composition failed (code {r.returncode})")
            logger.error(f"STDERR: {r.stderr[-1000:]}")
            raise RuntimeError("FFmpeg composition error")
    except Exception as e:
        logger.error(f"❌ Composition error: {e}")
        raise

    size_mb = os.path.getsize(output) / 1024 / 1024
    logger.info(f"✅ Short ready! | {final_dur:.1f}s | {size_mb:.1f} MB | {output}")


