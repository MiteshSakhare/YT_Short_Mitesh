def clean_text(text: str) -> str:
    """Clean and normalize text for TTS and subtitles."""
    # Remove literal \n or /n that might have been generated by AI
    text = text.replace('\\n', ' ').replace('\\N', ' ').replace('/n', ' ').replace('/N', ' ')
    
    # Remove emojis using unicode ranges for emoticons, transport, symbols, and supplemental emojis
    text = re.sub(r'[\U0001f300-\U0001f650\U0001f680-\U0001f6A0\U0001f900-\U0001f9FF\u2600-\u26FF\u2700-\u27BF]', '', text)
        
    text = re.sub(r'\n+', ' ', text)
    text = re.sub(r'\s+', ' ', text)
    text = re.sub(r'\.{3,}', '...', text)
    text = re.sub(r'---?', '—', text)
    text = re.sub(r'═+|─+', '', text)  # Remove decorative lines
    text = re.sub(r'[“”]', '"', text)  # Normalize curly quotes
    text = re.sub(r'[‘’]', "'", text)  # Normalize curly single quotes
    return text.strip()


def normalize_speaker(name: str) -> str:
    """
    Map a speaker name to a canonical voice key.
    Handles aliases, partial matches, and common patterns.
    Character memory: always returns the same key for the same character.
    """
    n = name.lower().strip()

    # Remove parenthetical notes like (thinking), (entering)
    n = re.sub(r'\(.*?\)', '', n).strip()

    # Check aliases first (longest match first to avoid substring conflicts)
    for alias, canonical in sorted(config.CHARACTER_ALIASES.items(), key=lambda x: len(x[0]), reverse=True):
        if alias in n:
            return canonical

    # Direct match in VOICES dict
    for voice_key in config.VOICES:
        if voice_key != "_default" and voice_key in n:
            return voice_key

    # Partial match patterns
    patterns = [
        (["duke", "arcturus", "booming voice", "voice", "father"], "duke"),
        (["kaelen", "kael", "emperor"], "kaelen"),
        (["seraphina"], "seraphina"),
        (["vex", "commander"], "vex'ahlia"),
        (["inquisitor", "mara"], "mara"),
        (["malachar", "lord m"], "malachar"),
        (["aldric", "king", "lion"], "aldric"),
        (["herald", "envoy"], "herald"),
        (["oracle"], "oracle"),
        (["guard", "soldier"], "guard"),
        (["instructor", "teacher"], "instructor"),
        (["council"], "council"),
    ]
    for keys, value in patterns:
        if any(k in n for k in keys):
            return value

    return "_default"


def sec_to_ass(t: float) -> str:
    """Convert seconds to ASS subtitle format."""
    h = int(t // 3600)
    m = int((t % 3600) // 60)
    s = int(t % 60)
    cs = int((t % 1) * 100)
    return f"{h}:{m:02d}:{s:02d}.{cs:02d}"


def get_audio_duration(path: str) -> float:
    """Get audio file duration using ffprobe."""
    try:
        result = subprocess.run(
            ["ffprobe", "-v", "quiet", "-print_format", "json",
             "-show_format", path],
            capture_output=True, text=True, check=True
        )
        return float(json.loads(result.stdout)['format']['duration'])
    except Exception:
        return 1.0

